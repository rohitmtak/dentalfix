function renderServiceContent(data) {
  document.querySelector(".services-title").textContent = data.title;
  document.querySelector(".services-img").src = data.img;
  document.querySelector(".services-img").alt = data.title;

  const aboutSection = document.querySelector(".services-about");
  aboutSection.innerHTML = `
    <h2 class="services-about-title">Brief Introduction</h2>
    <div class="services-about-text">
      ${data.brief.split('\n\n').map(paragraph => `<p>${paragraph}</p>`).join('')}
    </div>

    <h2 class="services-about-title">Signs and Symptoms</h2>
    <ul class="services-about-text services-symptoms-list">${data.symptoms.map(item => `<li>${item}</li>`).join('')}</ul>

    <h2 class="services-about-title">Procedure</h2>
    <div class="services-procedure">
      ${data.procedure.map((step, index) => {
    return `
        <div class="services-procedure-step">
          <div class="services-procedure-content">
            <h3 class="services-procedure-step-title">
              ${step.step}
            </h3>
            <p class="services-procedure-step-detail">${step.detail}</p>
          </div>
        </div>
      `;
  }).join('')}
    </div>

    <h2 class="services-about-title">Frequently Asked Questions</h2>
    <div class="services-faqs">
      ${data.faqs.map((faq, index) => `
        <div class="services-faq-item">
          <button class="services-faq-question" aria-expanded="false" aria-controls="faq-answer-${index}">
            ${faq.q}
            <span class="services-faq-icon">+</span>
          </button>
          <div class="services-faq-answer" id="faq-answer-${index}" hidden>
            <p>${faq.a}</p>
          </div>
        </div>
      `).join('')}
    </div>
  `;

  // Add click handlers for FAQ items
  document.querySelectorAll('.services-faq-question').forEach(button => {
    button.addEventListener('click', () => {
      const expanded = button.getAttribute('aria-expanded') === 'true';
      button.setAttribute('aria-expanded', !expanded);

      const answer = button.nextElementSibling;
      const icon = button.querySelector('.services-faq-icon');

      if (expanded) {
        answer.hidden = true;
        icon.textContent = '+';
        button.classList.remove('active');
      } else {
        answer.hidden = false;
        icon.textContent = '−';
        button.classList.add('active');
      }
    });
  });
}

document.querySelectorAll(".services-list-item").forEach((item) => {
  item.addEventListener("click", function () {
    // Remove 'active' from all items
    document.querySelectorAll(".services-list-item.active").forEach((el) => {
      el.classList.remove("active");
    });
    // Add 'active' to the clicked item
    this.classList.add("active");
    
    const cat = this.getAttribute("data-category");
    const data = serviceData[cat];
    if (!data) return;
    renderServiceContent(data);
  });
});

window.addEventListener("DOMContentLoaded", () => {
  const firstKey = Object.keys(serviceData)[0];
  const firstData = serviceData[firstKey];
  renderServiceContent(firstData);
});
